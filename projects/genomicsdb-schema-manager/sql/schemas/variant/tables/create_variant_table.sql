-- LOOKUP INDEXES
CREATE INDEX IX_VARIANT_VARIANT__NIAGADS_ID ON Variant.Variant USING HASH(NIAGADS_ID);
CREATE INDEX IX_VARIANT_VARIANT__REF_SNP_ID ON Variant.Variant USING HASH(REF_SNP_ID) WHERE REF_SNP_ID IS NOT NULL;
CREATE INDEX IX_VARIANT_VARIANT__BIN_INDEX ON Variant.Variant USING GIST(BIN_INDEX);
CREATE INDEX IX_VARIANT_VARIANT__POSITION ON Variant.Variant(POSITION); 
CREATE INDEX IX_VARIANT_VARIANT__RUN_ID ON Variant.Variant(RUN_ID);
CREATE INDEX IX_VARIANT_VARIANT__EMBEDDING_RUN_ID ON Variant.Variant(EMBEDDING_RUN_ID) WHERE EMBEDDING_RUN_ID IS NOT NULL;

